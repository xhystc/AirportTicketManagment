package com.xhystc.airport.test;

public class Pair
{
	int w;
	int v;

	public Pair(int w,int v)
	{
		this.v=v;
		this.w=w;
	}
	public int getW()
	{
		return w;
	}

	public void setW(int w)
	{
		this.w = w;
	}

	public int getV()
	{
		return v;
	}

	public void setV(int v)
	{
		this.v = v;
	}
}

